const Tour = require('../models/tourModel');
const User = require('../models/userModel');
const Booking = require('../models/bookingModel');
const catchAsync = require('../utils/catchAsync');
const AppError = require('../utils/appError');

exports.getOverview = catchAsync(async (req, res, next) => {
  //Get tour data
  const tours = await Tour.find();
  //Build template
  //Render template

  res.status(200).render('overview', {
    title: 'All Tours',
    tours,
  });
});

exports.getTour = catchAsync(async (req, res, next) => {
  //get data from the requested tour (including reviews and guides)
  const tourSlug = req.params.slug;
  const tour = await Tour.findOne({ slug: tourSlug }).populate({
    path: 'reviews',
    fields: 'review rating user',
  });

  if (!tour) {
    next(new AppError('There is no tour with that name', 404));
  }

  //Build template
  // Render template using data from 1
  res.status(200).render('tour', {
    title: tour.name,
    tour,
  });
});

